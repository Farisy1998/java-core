public class Assignment {
    int i = 10;
    float f = 20.54f;
    char ch = 'x';
    String str = "I am the best";

    public static void main(String[] args) {
        Assignment assignment = new Assignment();
        System.out.println(assignment.i);
        System.out.println(assignment.f);
        System.out.println(assignment.ch);
        System.out.println(assignment.str);
    }
}
