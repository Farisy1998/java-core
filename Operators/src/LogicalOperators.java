
public class LogicalOperators {

	public static void main(String[] args) {
		// The logical operators are
		// && - logical AND
		// || - logical OR
		boolean x = true, y= false;
		
		if(x && y) {
			System.out.println("Both of the x and y are true");
		}
		else {
			System.out.println("x and y are different");
		}
	}

}
